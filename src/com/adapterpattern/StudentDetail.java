package com.adapterpattern;

public class StudentDetail 
{
	int marks;
	String name;
	
	public String getName()
	{
		return name;
	}
	
	public int getMarks()
	{
		return marks;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public void setMarks(int marks)
	{
		this.marks = marks;
	}
}
